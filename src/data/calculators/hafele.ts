export function interpolate(table: Record<string, Record<string, [number, number]>>, height: number, models: string[]) {
  const heights = Object.keys(table).map(Number).sort((a, b) => a - b);
  const h0 = heights[0], hN = heights[heights.length - 1];
  const clampedH = Math.max(h0, Math.min(hN, height));

  let lo = h0, hi = hN;
  for (let i = 0; i < heights.length - 1; i++) {
    if (clampedH >= heights[i] && clampedH <= heights[i + 1]) { lo = heights[i]; hi = heights[i + 1]; break; }
  }
  const t = lo === hi ? 0 : (clampedH - lo) / (hi - lo);

  const result: Record<string, [number, number]> = {};
  for (const m of models) {
    const rLo = table[lo.toString()]?.[m];
    const rHi = table[hi.toString()]?.[m];
    if (!rLo) continue;
    const r = rHi ? [
      rLo[0] + t * (rHi[0] - rLo[0]),
      rLo[1] + t * (rHi[1] - rLo[1])
    ] : rLo;
    result[m] = r as [number, number];
  }
  return result;
}

export function findModel(ranges: Record<string, [number, number]>, weight: number) {
  const models = Object.keys(ranges);
  for (const m of models) {
    if (weight >= ranges[m][0] && weight <= ranges[m][1]) return m;
  }
  return null;
}
